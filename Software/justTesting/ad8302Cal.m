% Normalise to 0:180
normPhase = data(:,3) + 1;
normPhase = normPhase - 90;
normPhase = normPhase * 0.95;
normPhase = normPhase + 90;

frequency = data(:,1);
% For deg > 90
M1 = 0.012;
B1 = -1.5; 

% For deg < 90
M2 = 0.008;
B2 = -2;

phaseComp = [];

for i = 1:length(normPhase)
    if normPhase(i) >= 90
        phaseComp(i) = M1 * frequency(i) + B1;
    elseif normPhase(i) < 90
        phaseComp(i) = -(M2 * frequency(i) + B2);
    end
end

compWeight = [];

for i = 1:length(normPhase)
    if normPhase(i) > 160
        compWeight(i) = (normPhase(i) - 160) * 0.05;
    elseif normPhase(i) < 20
        compWeight(i) = (20 - normPhase(i)) * 0.05;
    else
        compWeight(i) = 0;
    end
end

corrPhase = normPhase + phaseComp' .* compWeight';

hold on;
plot(normPhase)
plot(corrPhase)
